private const val ARG_PARAM1 = "param1"
private const val ARG_PARAM2 = "param2"

/**
 * A simple [Fragment] subclass.
 * Use the [Fragment1.newInstance] factory method to
 * create an instance of this fragment.
 */
class Fragment1 : Fragment() {
    // TODO: Rename and change types of parameters
    private var param1: String? = null
    private var param2: String? = null

    private lateinit var binding: Fragment1Binding

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        arguments?.let {
            param1 = it.getString(ARG_PARAM1)
            param2 = it.getString(ARG_PARAM2)
        }
    }


    private val tasks = mutableListOf<String>()
    private lateinit var taskAdapter: TaskAdapter

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        binding = Fragment1Binding.inflate(inflater, container, false)

        taskAdapter = TaskAdapter(requireContext(), tasks)
        binding.listView.adapter = taskAdapter

        binding.listView.setOnItemClickListener { parent, view, position, id ->
            val selectedTask = tasks[position]

            val bundle = Bundle().apply {
                putString("selectedTask", selectedTask)
            }

            findNavController().navigate(
                R.id.action_fragment1_to_fragment22,
                bundle
            )
        }

        binding.buttonAddTask.setOnClickListener {
            val taskDescription = binding.editTextTask.text.toString()
            if (taskDescription.isNotBlank()) {
                tasks.add(taskDescription)
                taskAdapter.notifyDataSetChanged()
                binding.editTextTask.text.clear()
            }
        }

        return binding.root
    }


    companion object {
        /**
         * Use this factory method to create a new instance of
         * this fragment using the provided parameters.
         *
         * @param param1 Parameter 1.
         * @param param2 Parameter 2.
         * @return A new instance of fragment Fragment1.
         */
        // TODO: Rename and change types and number of parameters
        @JvmStatic
        fun newInstance(param1: String, param2: String) =
            Fragment1().apply {
                arguments = Bundle().apply {
                    putString(ARG_PARAM1, param1)
                    putString(ARG_PARAM2, param2)
                }
            }
    }
}